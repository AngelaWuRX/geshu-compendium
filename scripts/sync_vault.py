#!/usr/bin/env python3
"""Convert the publishable subset of the Obsidian vaults into docs/.

One-way: the vaults are the editing home, docs/notes/ is generated output. Run
after editing notes, then commit the result.

    python3 scripts/sync_vault.py            # convert
    python3 scripts/sync_vault.py --check    # report drift, write nothing
    python3 scripts/sync_vault.py --section networks

Exit codes:
    0  clean
    1  drift (--check only)
    2  policy violation — a denylisted path or a private-info pattern
    3  refused to clobber a file that isn't ours

The policy gate is the point of this script. Emission is two-phase — render
everything, verify everything, only then write — so a violation anywhere leaves
no partial output behind.

Stdlib only (tomllib needs Python >= 3.11).
"""

