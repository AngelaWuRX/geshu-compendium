# ── Obsidian → Material callout mapping ──────────────────────────────────────
# Material renders `!!!` static, `???` collapsed, `???+` collapsible-but-open.
# The trailing `-` on `> [!question]-` is what makes the Q&A cards collapse, so
# the fold suffix is load-bearing, not decoration.
CALLOUT_TYPES = {
    "note": "note", "info": "info", "tip": "tip", "warning": "warning",
    "danger": "danger", "question": "question", "example": "example",
    "abstract": "abstract", "success": "success", "bug": "bug", "quote": "quote",
    "failure": "failure", "hint": "tip", "todo": "info", "caution": "warning",
    "attention": "warning", "error": "danger", "summary": "abstract",
    "faq": "question",
    "important": "tip",   # closest Material colour/semantic
    "goal": "success",    # invented in the vault; no Material equivalent
}

CALLOUT_HEAD = re.compile(
    r"^(?P<indent>(?:> ?)*)> ?\[!(?P<type>[\w-]+)\](?P<fold>[+-]?) ?(?P<title>.*)$"
)
EMBED = re.compile(r"!\[\[(?P<target>[^\]|]+?)(?:\|(?P<mod>[^\]]+))?\]\]")
WIKILINK = re.compile(
    r"(?<!!)\[\[(?P<target>[^\]|#]+?)(?:#(?P<head>[^\]|]+))?(?:\|(?P<alias>[^\]]+))?\]\]"
)
PARENT_CHILD = re.compile(r"^(?P<kind>Parent|Children):\s*(?P<rest>.+)$")
# Accepts the malformed single-bracket `Children: [RadixSort]` in the CS vault.
LINKISH = re.compile(r"\[\[?([^\]\[]+?)\]?\]")
FENCE = re.compile(r"^\s*(`{3,}|~{3,})")
TABLE_ROW = re.compile(r"^\s*\|.*\|\s*$")


